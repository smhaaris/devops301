package com.mindtree.cpe.exception;

import org.springframework.web.bind.annotation.ResponseStatus;
import org.springframework.http.HttpStatus;

@ResponseStatus(HttpStatus.NOT_FOUND)
public class JenkinsNotFoundException extends Error {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public JenkinsNotFoundException() {
		super();
	}

	public JenkinsNotFoundException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace); 
	}

	public JenkinsNotFoundException(String message, Throwable cause) {
		super(message, cause); 
	}

	public JenkinsNotFoundException(String message) {
		super(message); 
	}

	public JenkinsNotFoundException(Throwable cause) {
		super(cause); 
	}
}
